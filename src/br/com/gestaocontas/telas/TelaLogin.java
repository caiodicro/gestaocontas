// MÉTODO PARA VALIDAÇÃO DE USUÁRIO E ACESSO À TELA PRINCIPAL
    private void logar() {
        String sqlLogin = "SELECT id_usuario, nome_usuario FROM tb_usuarios WHERE login_usuario = ? AND senha_usuario = ?";
        String sqlLogAcesso = "INSERT INTO tb_log_acessos (id_usuario, operacao_log) VALUES (?, 'entrou')";

        // VALIDAÇÃO DE CAMPOS OBRIGATÓRIOS
        if (txtUsuario.getText().isEmpty() || txtSenha.getPassword().length == 0) {
            JOptionPane.showMessageDialog(null, "Preencha usuário e senha!");
            return;
        }

        try (Connection conexao = Conexao.conectar()) {

            conexao.setAutoCommit(false);

            try (PreparedStatement pstLogin = conexao.prepareStatement(sqlLogin)) {
                pstLogin.setString(1, txtUsuario.getText());
                pstLogin.setString(2, new String(txtSenha.getPassword()));

                try (ResultSet rs = pstLogin.executeQuery()) {

                    if (!rs.next()) {
                        JOptionPane.showMessageDialog(null, "Usuário e/ou senha inválido(s)!");
                        return;
                    }

                    int idUsuario = rs.getInt("id_usuario");
                    String nomeUsuario = rs.getString("nome_usuario");

                    // REGISTRA LOG
                    try (PreparedStatement pstLog = conexao.prepareStatement(sqlLogAcesso)) {
                        pstLog.setInt(1, idUsuario);

                        int acesso = pstLog.executeUpdate();

                        // SÓ CONTINUA SE O LOG FOI INSERIDO
                        if (acesso > 0) {
                            conexao.commit();

                            TelaMenuPrincipal principal = new TelaMenuPrincipal();
                            TelaMenuPrincipal.lblUsuarioLogado.setText(String.valueOf(idUsuario));
                            TelaMenuPrincipal.lblUsuario.setText(nomeUsuario);
                            principal.setVisible(true);
                            this.dispose();

                        } else {
                            conexao.rollback();
                            JOptionPane.showMessageDialog(null, "Falha ao registrar log de acesso!");
                        }
                    }
                }
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Erro ao logar: " + e.getMessage());
        }
    }

