    // MÉTODO PARA TESTAR STATUS DE CONEXÃO COM BANCO DE DADOS AO INICIAR PROGRAMA
    private void status_bd() {
        Connection conexao = null;
        try {
            conexao = Conexao.conectar();
            if (conexao != null) {
                lblStatusBD.setIcon(new javax.swing.ImageIcon(getClass().getResource("/br/com/gestaocontas/icones/bd_ok_64px.png")));
            }
        } catch (Exception e) {
            lblStatusBD.setIcon(new javax.swing.ImageIcon(getClass().getResource("/br/com/gestaocontas/icones/bd_erro_64px.png")));
            JOptionPane.showMessageDialog(null, "Erro: Conexão com Banco de Dados:\n" + e.getMessage());
        } finally {
            try {
                if (conexao != null && !conexao.isClosed()) {
                    conexao.close();
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Erro ao fechar conexão:\n" + ex.getMessage());
            }
        }
    }

